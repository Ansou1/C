int	my_square_root(int nb)
{
  int	i;

  i = 0;
  if (nb < 0)
    {
      return (0);
    }
  while (i * i <= nb)
    {
      if (i * i == nb)
	{
	  return (i);
	}
      i = i + 1;
    }
  return (0);
}
