int	my_str_isupper(char *str)
{
  int	i;
  int	result;

  i = 0;
  result = 1;
  while (str[i] != '\0')
    {
      if (!(str[i] >= 65 && str[i] <= 90))
	{
	  result = 0;
	}
      i = i + 1;
    }
  return (result);
}
